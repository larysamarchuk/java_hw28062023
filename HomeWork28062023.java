package Homework.New;

public class HomeWork28062023 {
    public static void main(String[] args) {
        // Инициализация переменной
        int number = 1;

        // Цикл for для вывода чисел
        for (int i = 0; i < 10; i++) {
            System.out.print(number + " ");
            number *= 2; // Умножаем число на 2 в каждой итерации
        }
    }
}
